int	ft_memcmp(const void *s1, const void *s2, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		if (s1[i] != s2[i])
			return ((int) s1[i] - (int) s2[i]);
		i++;
	}
	return (0);
}
