void	*ft_memchr(const char *s, int c, int n)
{
	int	i;

	while (c > 256)
		c -= 256;
	i = 0;
	while (i < n)
	{
		if (s[i] == (char) c)
			return ((void *) &s[i]);
		i++;
	}
	return (0);
}

