#include "libft.h"

int	ft_isalnum(int c)
{
	char	ch;

	ch = (char) c;
	if (ch <= 'Z' && ch >= 'A')
		return (1);
	else if (ch <= 'z' && ch >= 'a')
		return (1);
	else if (ch <= '9' && ch >= '0')
		return (1);
	return (0);
}
